let contador = 0;
function opciones () {
	contador++;
	if (contador%2 == 0) {
		var ocultar = document.getElementById('boton').nextElementSibling;
		ocultar.style.cssText = 'display: none';
	} else {
		var mostrar = document.getElementById('boton').nextElementSibling;
		mostrar.style.cssText = 'display: block';
	}
}
